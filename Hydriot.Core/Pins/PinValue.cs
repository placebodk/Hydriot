namespace Hydriot.Core.Pins
{
    /// <summary>
    /// Represents a value of a pin, either Low or High
    /// </summary>
    public enum PinValue
    {
        /// <summary>
        /// The value of the pin is Low
        /// </summary>
        Low = 0,

        /// <summary>
        /// The value of the pin is High
        /// </summary>
        High = 1
    }
}